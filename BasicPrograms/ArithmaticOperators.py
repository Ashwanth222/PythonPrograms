# What does << do in Python?
# As you can see << ( left shift operator ) is used to multiply the right hand side value
# by 2^( left hand side value ). Similarly, >> ( right shift operator ) is used to divide the
# right hand side value by 2^( left hand side value ).

a = 5
b = 2
c =a<<b
d = a>>b
print(c)
print(d)